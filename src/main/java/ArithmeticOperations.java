public class ArithmeticOperations {

	// Write a static method that adds num1 to num2
	public static int add(int num1, int num2){
		return (num1 + num2);
	}

	// Write a static method that subtracts num2 from num1
	public static int subtract(int num1, int num2){
		return(num1-num2);
	}

	// Write a static method that multiplies num1 by num2
	public static double multiply(double num1, double num2){
		return(num1*num2);
	}

	// Write a static method that divides the numerator by the denominator
public static int divide(int numerator, int denominator){
		return(numerator/denominator);
}
}
